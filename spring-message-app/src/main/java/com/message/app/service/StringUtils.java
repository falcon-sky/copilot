package com.message.app.service;

public class StringUtils {
  /**
   * @param a
   * @param b
   * @return
   */
  public String append(String a, String b) {
    return a + b;
  }
  public String concat(String a, String b) {
    return a + b;
  }

  String toLowerCase(String a) {
    return a.toLowerCase();
  }

}
